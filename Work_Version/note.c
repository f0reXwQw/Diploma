int find_section(void* elf, unsigned char* section_name)
{
	Elf32_Ehdr* ehdr;
	Elf32_Shdr* shdr;

	ehdr = (Elf32_Ehdr*) elf;
	shdr = (Elf32_Shdr*) (elf + ehdr->e_shoff);
	for (int i = 0; i < ehdr->e_shnum; i++)
		if (!strcmp(section_name, elf + (shdr[ehdr->e_shstrndx].sh_offset + shdr[i].sh_name)))
			return i;
	return -1;
}

void encrypt_section(void* elf, int section_ind, unsigned char* key)
{
	Elf32_Ehdr* ehdr;
	Elf32_Shdr* shdr;
	ehdr = (Elf32_Ehdr*) elf;
	shdr = (Elf32_Shdr*) (elf + ehdr->e_shoff);
	for (int i = shdr[section_ind].sh_offset; i < (shdr[section_ind].sh_offset + shdr[section_ind].sh_size); i++)
		*((unsigned char *)(elf + i)) = *((unsigned char*)(elf + i)) ^ key[(i - shdr[section_ind].sh_offset) % 16];
}

int find_note_segment(Elf32_Phdr const *phdr, int count)
{
	for (int i = 0; i < count; i++)
	{
		if (phdr[i].p_type == PT_NOTE)
			return i;
	}
	return -1;
}

static void bail(char const *prefix, char const *msg)
{
	fprintf(stderr, "%s: %s\n", prefix, msg);
	exit(EXIT_FAILURE);
}

void* map_file(char const *file_name)
{
	struct stat file_stat;
	void* ptr;
	int fd;

	fd = open(file_name, O_RDWR);
	if (fd < 0)
		bail(file_name, strerror(errno));
	if (fstat(fd, &file_stat))
		bail(file_name, strerror(errno));
	if (!S_ISREG(file_stat.st_mode))
		bail(file_name, "not an ordinary file.");
	ptr = mmap(NULL, file_stat.st_size, PROT_READ | PROT_WRITE, MAP_SHARED, fd, 0);
	if (ptr == MAP_FAILED)
		bail(file_name, strerror(errno));
	close(fd);
	return ptr;
}

int fsize(unsigned char* file_name)
{
	struct stat file_stat;
	if (stat(file_name, &file_stat))
		return -1;
	return file_stat.st_size;
}

int create_file_copy(char* source_file_name, char* new_file_name)
{
	char* source_file_content;
	int file_copy;
	int source_file;
	int source_file_size;

	source_file = open(source_file_name, O_RDONLY);
	if (source_file < 0)
		return -1;
	source_file_size = fsize(source_file_name);
	if (source_file_size < 0)
		return -1;
	source_file_content = malloc(source_file_size);
	if (!source_file_content)
		return -1;
	if (read(source_file, source_file_content, source_file_size) < 0)
		return -1;
	file_copy = open(new_file_name, O_WRONLY | O_CREAT | O_TRUNC | O_CLOEXEC, 0777);
	if (file_copy < 0)
		return -1;
	if (write(file_copy, source_file_content, source_file_size) < 0)
		return -1;
	
	close(file_copy);
	close(source_file);
	free(source_file_content);
	return 0;
}

unsigned int calc_vaddr(Elf32_Phdr* phdr, int hdr_num)
{
	unsigned int vaddr = 0;
	for (int i = 0; i < hdr_num; i++)
		if (vaddr < phdr[i].p_vaddr + phdr[i].p_memsz)
			vaddr = phdr[i].p_vaddr + phdr[i].p_memsz;
	return vaddr;
}

void hex2bin(unsigned char* hex_string, unsigned char* bin_string)
{
    unsigned int hex_len = strlen(hex_string);

    for (int i = 0, j = 0; j < (hex_len / 2); i += 2, j++) 
        bin_string[j] = (hex_string[i] % 32 + 9) % 25 * 16 + (hex_string[i+1] % 32 + 9) % 25;
}

